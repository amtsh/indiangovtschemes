public class Description {

  private String what;
  private String who;
  private String date;

  public void setWhat(String what) {
    this.what = what;
  }

  public void setWho(String who) {
    this.who = who;
  }

  public void setDate(String date) {
    this.date = date;
  }

  public String getWhat() {
    return this.what;
  }

  public String getWho() {
    return this.who;
  }

  public String getDate() {
    return this.date;
  }
}
